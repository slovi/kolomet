function ToggleTypes() {
	var TypeUl = document.getElementById("Types");
	TypeUl.setAttribute("class","");
	TypeUl.setAttribute("className","");
	
	var BrandUl = document.getElementById("Brands");
	BrandUl.setAttribute("class","hidden");
	BrandUl.setAttribute("className","hidden");
		
	var TADH = document.getElementById("TypeArrowDown");
	TADH.setAttribute("class","");
	TADH.setAttribute("className","");
		
	var BARH = document.getElementById("BrandArrowRight");
	BARH.setAttribute("class","");
	BARH.setAttribute("className","");
		
	var TARA = document.getElementById("TypeArrowRight");
	TARA.setAttribute("class","hidden");
	TARA.setAttribute("className","hidden");
		
	var BADA = document.getElementById("BrandArrowDown");
	BADA.setAttribute("class","hidden");
	BADA.setAttribute("className","hidden");
	}
	
